#Arithmetic Operators
x = 2
y = 5

total = x + y
print(total)

total = x - y
print(total)

total = x * y
print(total)

total = y / x
print(total)

total = y % x
print(total)

total = y ** x
print(total)

#comparison operators
a = 30
b = 60

out = a < b
print(out)

out = a > b
print(out)

out = (a == b)
print(out)

out = (a != b)
print(out)

out = (a <= b)
print(out)

out = (a >= b)
print(out)

#Assignment Operators

c = 0
d = 10

c += d  # c = c + d
print(c)

c -= d  # c = c - d
print(c)

